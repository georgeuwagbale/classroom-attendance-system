    public void IdentifyFingerPrint() {
        found = false;

        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/CAS","root", "example");
            PreparedStatement ps = con.prepareStatement("select * from User");

            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                byte templateBuffer[] = rs.getBytes("FingerPrint");
                DPFPTemplate referenceTemplate = DPFPGlobal.getTemplateFactory().createTemplate(templateBuffer);
                setTemplate(referenceTemplate);
                DPFPVerificationResult result = Checker.verify(FingerPrintFeatureVerification, getTemplate());

                if (result.isVerified()) {
                    found = true;
                    //System.out.print(rs.getString("FirstName"));
                    System.out.println(rs.getString("LastName"));
                    //userID = rs.getInt("UserID");
                    System.out.println(StudentSignInView.checkForMultipleSigning(rs.getInt("UserID")));
                    if (!StudentSignInView.checkForMultipleSigning(rs.getInt("UserID"))){
                        StudentSignInView.signAttendance(rs.getInt("UserID"));
                        JOptionPane.showMessageDialog(rootPane, "Welcome to class " + rs.getString("FirstName")
                                + " " + rs.getString("LastName")
                                ,"Signed in", 1);
                    }else{
                        JOptionPane.showMessageDialog(rootPane, "User already signed into class");
                    }
                    
                }

            }

            if (!found) {
                JOptionPane.showMessageDialog(rootPane, "No user found");
            }
            con.close();
        } catch (Exception e) {
            
        }
    }

