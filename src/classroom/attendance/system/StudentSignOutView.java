    public void populateStudentTable(int courseID){
        DefaultTableModel td = (DefaultTableModel)student_table.getModel();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/CAS", "root", "example");
            PreparedStatement ps = con.prepareStatement("select * from User where UserID in("
                    + "select UserID from Student where StudentID in("
                    + "select StudentID from StudentAttendance where AttendanceID in("
                    + "select AttendanceID from Attendance where CourseID=?"
                    + ")"
                    + ")"
                    + ")");
            ps.setInt(1, courseID);
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()){
                String[] data = {
                    "",
                    "Sign out"
                };
                  
                String name = rs.getString("FirstName") + " " + rs.getString("LastName");
                userNameDict.put(name, rs.getInt("UserID"));
                data[0] = name;
                
                boolean flag = true;
                int td_row = td.getRowCount();
                for(int i=0; i<td_row; i++){
                    //System.out.println(data[1]);
                    if(td.getValueAt(i, 0).equals(data[0]) ){
                        //String courses = (String) HODAllocateCourseToLecturerView.td.getValueAt(i, 1);
                        
                        td.setValueAt(data[0], i, 0);
                        flag = false;
                    }
                    
                }
                if(flag){
                    td.addRow(data);
                }
            }
            
        }catch(Exception e){
            System.out.println(e);
        }
    }
    
