    private void student_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_student_tableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel td = (DefaultTableModel)student_table.getModel();
        int row = student_table.getSelectedRow();
        int userID = 0;
        try{
            String userName = String.valueOf(td.getValueAt(row, 0));
            if (userName != null){
                userID = this.userNameDict.get(userName);

                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/CAS", "root", "example");
                PreparedStatement ps = con.prepareStatement("delete from StudentAttendance where StudentID in("
                        + "select StudentID from Student where UserID=?"
                        + ") and AttendanceID in("
                        + "select AttendanceID from Attendance where CourseID=?"
                        + ")");
                ps.setInt(1, userID);
                ps.setInt(2, this.courseID);
                ps.execute();
                
                JOptionPane.showMessageDialog(rootPane, "Student signed out");
                populateStudentTable(this.courseID);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_student_tableMouseClicked

